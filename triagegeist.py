# 1. MEWS Score (Modified Early Warning Score) — real clinical tool!
def compute_mews(row):
    score = 0
    # Respiratory rate
    rr = row['RESPR']
    if pd.notna(rr):
        if rr < 9: score += 2
        elif rr <= 14: score += 0
        elif rr <= 20: score += 1
        elif rr <= 29: score += 2
        else: score += 3
    # Heart rate
    hr = row['PULSE']
    if pd.notna(hr):
        if hr < 40: score += 2
        elif hr <= 50: score += 1
        elif hr <= 100: score += 0
        elif hr <= 110: score += 1
        elif hr <= 129: score += 2
        else: score += 3
    # Systolic BP
    sbp = row['BPSYS']
    if pd.notna(sbp):
        if sbp < 70: score += 3
        elif sbp <= 80: score += 2
        elif sbp <= 100: score += 1
        elif sbp <= 199: score += 0
        else: score += 2
    # O2 sat
    o2 = row['POPCT']
    if pd.notna(o2):
        if o2 < 85: score += 3
        elif o2 <= 89: score += 2
        elif o2 <= 93: score += 1
    return score

